package entity;

public class Member {
	
	//フィールド
	private int id;
	private String name;
	private String email;
	
	//デフォルトコンストラクタ
	public Member() {}
	
	//引数有りコンストラクタ
	public Member(int id, String name, String email) {
		super();
		this.id = id;
		this.name = name;
		this.email = email;
	}

	//ゲッターセッター
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	};
	
}
